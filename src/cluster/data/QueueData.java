  public static double getWeight(int[] queueNums, int idx){
    if (idx >= queueNums.length) {
      System.err.println("getWeight: out of array range");
      return 1.0;
    }
    return (double) queueNums[idx];
  }
  
  public static double getAverageWeight(int[] queueNums){
    if (queueNums==null || queueNums.length<1) {
      System.err.println("getAverageWeight: null or empty input");
      return 1.0;
    }
    double average = 0.0;
    for (int i=1;i<queueNums.length; i++)
      average += queueNums[i];
    average/= queueNums.length;
    
    return average;
  }
